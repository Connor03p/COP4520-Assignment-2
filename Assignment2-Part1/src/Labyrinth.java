public class Labyrinth 
{
    private boolean isEmpty = true;
    private boolean hasCupcake = true;
    private boolean everyoneEntered = false;

    public boolean getIsEmpty()
    {
        return isEmpty;
    }

    public void setIsEmpty(boolean value)
    {
        isEmpty = value;
    }


    public boolean getHasCupcake()
    {
        return hasCupcake;
    }

    public void setHasCupcake(boolean value)
    {
        hasCupcake = value;
    }

    public boolean getEveryoneEntered()
    {
        return everyoneEntered;
    }

    public void setEveryoneEntered(boolean value)
    {
        everyoneEntered = value;
    }
}
